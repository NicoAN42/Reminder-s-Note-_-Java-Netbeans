 /////2.Setting Pengaturan Jam sesuai laptop
    
      private void jam() {
          ActionListener taskPerformer=new ActionListener(){ ////klik ALT+ENTER -> import ActionListener ->TOP
             
            public void actionPerformed(ActionEvent evt){ ////klik ALT+ENTER -> import actionPerformed ->TOP
                String nol_bulan="";
                String nol_hari="";
                String nol_jam="";
                String nol_menit="";
                String nol_detik="";
                Calendar dt=Calendar.getInstance(); ////untuk mengambil informasi waktu
 
                int nilai_jam=dt.get(Calendar.HOUR_OF_DAY);         ////3.Calendar.HOUR_OF_DAY digunakan untuk settingan jam
                int nilai_menit=dt.get(Calendar.MINUTE);            ////4.Calendar.MINUTE digunakan untuk settingan menit
                int nilai_detik=dt.get(Calendar.SECOND);            ////5.Calendar.SECOND digunakan untuk settingan detik
                
               
                if(nilai_jam<=9){
                    nol_jam="0";
                }
                if(nilai_menit<=9){
                    nol_menit="0";
                }
                if(nilai_detik<=9){
                    nol_detik="0";
                }
                
                String jam=nol_jam+Integer.toString(nilai_jam);
                String menit=nol_menit+Integer.toString(nilai_menit);
                String detik=nol_detik+Integer.toString(nilai_detik);
              jammm.setText(jam+":"+menit+":"+detik);              ////6.Label dengan variabel "jammm" akan berubah sesuai settingan jam menit dan detik

            }
            };
            new javax.swing.Timer(1000,taskPerformer).start(); ////untuk start
    }
    
    
////7.Untuk inputan file yang nantinya akan digunakan ringtone reminder
      
    void player(){
        InputStream in; ////apabila merah -> ALT+ENTER ->input stream ->TOP
        try {   ////try {}catch(Exception e){}
            in = new FileInputStream(new File(vpath.getText()));
            AudioStream audios = new AudioStream(in);
            AudioPlayer.player.start(audios);
        } catch (Exception e) {
            e.printStackTrace();
        }            
    }
    
    
