    private void jammmPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jammmPropertyChange
        
        if (jCheckBox1.isSelected()==true) {    ////apabila checkbox di pilih maka textfield dengan variable "vket & vjam" tidak dapat di edit
            vket.setEnabled(false);
            vjam.setEnabled(false);
            if (vjam.getText().equals(jammm.getText())) {
                player();
                JOptionPane.showMessageDialog(rootPane, ""+vket.getText()+"");
            }else{
            }
            
        }else{
            vket.setEnabled(true);  ////apabila checkbox tidak di pilih maka textfield dengan variable "vket & vjam" dapat di edit
            vjam.setEnabled(true);
        }
        
    }//GEN-LAST:event_jammmPropertyChange

